    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField3.getText();
        String physics = jTextField4.getText();
        String maths = jTextField5.getText();
        String em = jTextField6.getText();
        String dbms = jTextField7.getText();
        String os = jTextField8.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "bhaikala12");
            
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("SELECT * FROM student where rollno = '"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno, physics, em, maths, dbms, os) values('"+rollno+"', '"+physics+"', '"+em+"', '"+maths+"', '"+dbms+"', '"+os+"')");
                JOptionPane.showMessageDialog(null, "Result Inserted Successfully!");
                
                setVisible(false);
                new insertnewresult().setVisible(true);
          }
            else{
                JOptionPane.showMessageDialog(null, "Student is not registered!");
            }
            
        }
        catch(Exception e){
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

