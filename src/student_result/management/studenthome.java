    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try{
            String rollno = jTextField3.getText();
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "bhaikala12");
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(" select * from student inner join result where student.rollno = '"+rollno+"' and result.rollno='"+rollno+"'");
        if(rs.next()){
//            student
            jTextField1.setText(rs.getString(2));
            jTextField2.setText(rs.getString(4));
            jTextField4.setText(rs.getString(3));
            jTextField5.setText(rs.getString(5));
            jTextField6.setText(rs.getString(6));
//            Result
            jTextField7.setText(rs.getString(8));
            jTextField8.setText(rs.getString(10));
            jTextField9.setText(rs.getString(9));
            jTextField10.setText(rs.getString(11));
            jTextField11.setText(rs.getString(12));
//            converting subject marks from string to int;
             int m1 = Integer.parseInt(jTextField7.getText());
             int m2 = Integer.parseInt(jTextField8.getText());
             int m3 = Integer.parseInt(jTextField9.getText());
             int m4 = Integer.parseInt(jTextField10.getText());
             int m5 = Integer.parseInt(jTextField11.getText());
             
             int totalres = m1+m2+m3+m4+m5;
             double percentage = (totalres/5);
             
             jTextField13.setText(String.valueOf(totalres));
             jTextField14.setText(String.valueOf(percentage));
             
             if(percentage < 35){
                 jTextField15.setText("FAIL");
             }
             else jTextField15.setText("PASS");
        }
        else{
            JOptionPane.showMessageDialog(null, rs);
        }
        }
        catch(Exception e){
            JOptionPane.showConfirmDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

